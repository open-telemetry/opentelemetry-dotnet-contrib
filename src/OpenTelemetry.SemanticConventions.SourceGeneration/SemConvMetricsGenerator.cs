using OpenTelemetry.SemanticConventions.SourceGeneration;
using System.Linq;
using System.Threading;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using OpenTelemetry.SemanticConventions.SourceGeneration.Emitters;
using OpenTelemetry.SemanticConventions.SourceGeneration.Extractors;
using OpenTelemetry.SemanticConventions.SourceGeneration.Models;

namespace OpenTelemetry.SemanticConventions.SourceGeneration;

[Generator(LanguageNames.CSharp)]
public sealed class SemConvMetricsGenerator : IIncrementalGenerator
{
    internal const string StableAttributeFullName =
        "OpenTelemetry.SemanticConventions.SourceGeneration.SemanticConventionMetricsAttribute";

    internal const string IncubatingAttributeFullName =
        "OpenTelemetry.SemanticConventions.SourceGeneration.SemanticConventionIncubatingMetricsAttribute";

    internal const string AttributeFullName = StableAttributeFullName;

    private const string StableAttributeSource = """
        // <auto-generated/>
        #nullable enable
        namespace OpenTelemetry.SemanticConventions.SourceGeneration;

        [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
        [global::System.Diagnostics.Conditional("QYL_SEMCONV_USAGES")]
        internal sealed class SemanticConventionMetricsAttribute : global::System.Attribute
        {
            public SemanticConventionMetricsAttribute(string prefix)
            {
                Prefix = prefix;
            }

            public string Prefix { get; }
        }
        """;

    private const string IncubatingAttributeSource = """
        // <auto-generated/>
        #nullable enable
        namespace OpenTelemetry.SemanticConventions.SourceGeneration;

        [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
        [global::System.Diagnostics.Conditional("QYL_SEMCONV_USAGES")]
        internal sealed class SemanticConventionIncubatingMetricsAttribute : global::System.Attribute
        {
            public SemanticConventionIncubatingMetricsAttribute(string prefix)
            {
                Prefix = prefix;
            }

            public string Prefix { get; }
        }
        """;

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(static ctx =>
        {
            ctx.AddSource("SemanticConventionMetricsAttribute.g.cs", StableAttributeSource);
            ctx.AddSource("SemanticConventionIncubatingMetricsAttribute.g.cs", IncubatingAttributeSource);
        });

        var stableMarkers = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                StableAttributeFullName,
                static (node, _) => node is ClassDeclarationSyntax,
                static (ctx, ct) => ExtractMarker(ctx, StabilityFilter.StableOnly, ct))
            .WhereNotNull();

        var incubatingMarkers = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                IncubatingAttributeFullName,
                static (node, _) => node is ClassDeclarationSyntax,
                static (ctx, ct) => ExtractMarker(ctx, StabilityFilter.AllStabilities, ct))
            .WhereNotNull();

        context.RegisterSourceOutput(stableMarkers, static (spc, marker) =>
        {
            var file = MetricsEmitter.Generate(marker, RegistryLoader.Instruments);
            if (!file.IsEmpty)
                spc.AddSource(file.Name, file.Text);
        });

        context.RegisterSourceOutput(incubatingMarkers, static (spc, marker) =>
        {
            var file = MetricsEmitter.Generate(marker, RegistryLoader.Instruments);
            if (!file.IsEmpty)
                spc.AddSource(file.Name, file.Text);
        });
    }

    private static MetricsMarkerModel? ExtractMarker(
        GeneratorAttributeSyntaxContext context,
        StabilityFilter filter,
        CancellationToken cancellationToken)
    {
        cancellationToken.ThrowIfCancellationRequested();

        if (context.TargetSymbol is not INamedTypeSymbol typeSymbol)
            return null;

        var attributeData = context.Attributes.FirstOrDefault();
        if (attributeData is null || attributeData.ConstructorArguments.Length == 0)
            return null;

        if (attributeData.ConstructorArguments[0].Value is not string prefix)
            return null;

        if (string.IsNullOrWhiteSpace(prefix))
            return null;

        var ns = typeSymbol.ContainingNamespace.IsGlobalNamespace
            ? string.Empty
            : typeSymbol.ContainingNamespace.ToDisplayString();

        return new MetricsMarkerModel(ns, typeSymbol.Name, prefix, filter);
    }
}
